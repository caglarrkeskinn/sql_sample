-- Creating DEPARTMENT table

CREATE TABLE DEPARTMENT (
DEPARTMENT_ID INT NOT NULL PRIMARY KEY,
COURSE_ID INT NOT NULL,
DEPARTMENT_NAME VARCHAR(25) NOT NULL
);

-- Added Foreign Key (COURSE_ID) after created the COURSE table
ALTER TABLE DEPARTMENT
ADD CONSTRAINT FK_COURSE
FOREIGN KEY (COURSE_ID) REFERENCES COURSE(COURSE_ID);

-- Creating STUDENT table

CREATE TABLE STUDENT (
STUDENT_ID INT NOT NULL PRIMARY KEY,
FIRST_NAME VARCHAR2(25),
LAST_NAME VARCHAR2(25),
ADDRESS VARCHAR2(25),
DATE_OF_BIRTH DATE,
PHONE VARCHAR2(25),
GENDER VARCHAR2(25),
AGE INT);

-- Creating COURSE table

CREATE TABLE COURSE (
COURSE_ID INT NOT NULL PRIMARY KEY,
COURSE_NAME VARCHAR2(15),
SECTION INT,
SEMESTER VARCHAR(15));

-- Added Foreign Key (INSTRUCTOR_ID) after created the INSTRUCTOR table

ALTER TABLE COURSE
ADD CONSTRAINT FK_INSTRUCTOR
FOREIGN KEY (INSTRUCTOR_ID) REFERENCES INSTRUCTOR(INSTRUCTOR_ID);

-- Creating INSTRUCTOR table

CREATE TABLE INSTRUCTOR ( INSTRUCTOR_ID INT NOT NULL PRIMARY KEY,
                            FIRST_NAME VARCHAR2(25),
LAST_NAME VARCHAR2(25),
EMAIL VARCHAR2(25),
ADDRESS VARCHAR2(25),
DATE_OF_BIRTH DATE,
PHONE VARCHAR2(25),
GENDER VARCHAR2(25),
AGE INT);

