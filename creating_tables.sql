-- Creating FACULTY table

CREATE TABLE FACULTY (
FACULTY_ID INT NOT NULL PRIMARY KEY,
DEPARTMENT_ID INT NOT NULL,
STUDENT_ID INT NOT NULL,
FACULTY_NAME VARCHAR(25) NOT NULL,
DEAN VARCHAR(25)
);

